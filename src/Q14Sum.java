
public class Q14Sum {

	public static void main(String[] args) {

        int[] numbers = {2, 4, 6, 8, 10};
        int i = 0;
        int sum = 0;
        while (i < numbers.length) {
            sum += numbers[i];
            i++;
        }
        System.out.println("Sum: " + sum);
    }

		
		
	}


